import fno_backtester
result = fno_backtester.run_fno_backtest('RELIANCE')
print('Instrument:', result.get('instrument'))
print('Entry date:', result.get('target_date'))
print('Entry time:', result.get('entry_time'))
print('Entry price:', result.get('entry_price'))
print('Days held:', result.get('days_held'))
trade = result.get('trade_simulation', {})
print('Would trade:', trade.get('would_trade'))
print('Direction:', trade.get('direction'))
print('PnL:', trade.get('total_pnl'))
print('Exit reason:', trade.get('exit_reason'))
chart = result.get('chart', {})
print('Chart labels:', len(chart.get('labels', [])))
print('Entry chart idx:', result.get('entry_candle_idx'))
print('Exit chart idx:', result.get('exit_candle_idx'))
disc = result.get('discrepancy')
if disc:
    print('Direction correct:', disc['direction_correct'])
print('---')
